function processLanguage(lang) {
    const filePath = path.join(MESSAGES_DIR, `${lang}.json`);
    if (!fs.existsSync(filePath)) {
        console.error(`File not found: ${filePath}`);
        return;
    }

    try {
        const fileContent = fs.readFileSync(filePath, 'utf8');
        const data = JSON.parse(fileContent);

        if (data.services && data.services.items && data.services.items['company-limited']) {
            // Preserve existing title and summary
            const existingTitle = data.services.items['company-limited'].title;
            const existingSummary = data.services.items['company-limited'].summary;

            // Select content based on language
            const newContent = lang === 'th' ? THAI_CONTENT : EMPTY_CONTENT;

            // Update the object
            data.services.items['company-limited'] = {
                title: existingTitle,
                summary: existingSummary,
                ...newContent
            };

            // Write back to file with proper formatting (2 spaces for indentation)
            fs.writeFileSync(filePath, JSON.stringify(data, null, 2), 'utf8');
            console.log(`✅ Successfully updated company-limited content for ${lang}.json`);
        } else {
            console.warn(`⚠️ Warning: services.items['company-limited'] not found in ${lang}.json`);
        }
    } catch (error) {
        console.error(`❌ Error processing ${lang}.json:`, error.message);
    }
}

